/* Adding up item to array at the end*/
var myList = [1,2,3]
myList.push(4)
console.log(myList)

/* Adding up item to array at the beginning*/
myList.unshift("item")
console.log(myList)

/* Removing up item from arrays beginning*/
myList.shift()
console.log(myList)

/* Removing up item from arrays end*/
myList.pop()
console.log(myList)
